Cypress.Commands.add('visitComponent', storyPath => {
  const [type, component, story] = storyPath.split(':');
  const pathName = `selectedKind=${type}%7C${component}&selectedStory=${story}`;

  const iframeUrl = `/iframe?${pathName}`;

  cy.visit(iframeUrl);
});
